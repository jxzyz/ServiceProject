package com.service.bean;

public class Student {
	private String stu_email;
	private String stu_nickname;
	private String stu_password;
	private String stu_sex;
	private String stu_name;
	private String stu_level;
	private String stu_inter;
	public String getStu_email() {
		return stu_email;
	}
	public void setStu_email(String stu_email) {
		this.stu_email = stu_email;
	}
	public String getStu_nickname() {
		return stu_nickname;
	}
	public void setStu_nickname(String stu_nickname) {
		this.stu_nickname = stu_nickname;
	}
	public String getStu_password() {
		return stu_password;
	}
	public void setStu_password(String stu_password) {
		this.stu_password = stu_password;
	}
	public String getStu_sex() {
		return stu_sex;
	}
	public void setStu_sex(String stu_sex) {
		this.stu_sex = stu_sex;
	}
	public String getStu_name() {
		return stu_name;
	}
	public void setStu_name(String stu_name) {
		this.stu_name = stu_name;
	}
	public String getStu_level() {
		return stu_level;
	}
	public void setStu_level(String stu_level) {
		this.stu_level = stu_level;
	}
	public String getStu_inter() {
		return stu_inter;
	}
	public void setStu_inter(String stu_inter) {
		this.stu_inter = stu_inter;
	}
	
	
}
