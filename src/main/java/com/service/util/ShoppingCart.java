package com.service.util;

public class ShoppingCart {
	private String stu_email;
	private String cou_id;
	private double cou_price;
	private String cou_name;
	private String cou_grade;
	private String cou_subject;

	
	public String getStu_email() {
		return stu_email;
	}
	public void setStu_email(String stu_email) {
		this.stu_email = stu_email;
	}
	public String getCou_id() {
		return cou_id;
	}
	public void setCou_id(String cou_id) {
		this.cou_id = cou_id;
	}
	public double getCou_price() {
		return cou_price;
	}
	public void setCou_price(double cou_price) {
		this.cou_price = cou_price;
	}
	public String getCou_name() {
		return cou_name;
	}
	public void setCou_name(String cou_name) {
		this.cou_name = cou_name;
	}
	public String getCou_grade() {
		return cou_grade;
	}
	public void setCou_grade(String cou_grade) {
		this.cou_grade = cou_grade;
	}
	public String getCou_subject() {
		return cou_subject;
	}
	public void setCou_subject(String cou_subject) {
		this.cou_subject = cou_subject;
	}
	
	
}
